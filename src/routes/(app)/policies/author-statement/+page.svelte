		<h1 class="text-4xl font-bold text-gray-900 mb-4">CRediT Author Statement</h1>
		
		<div class="bg-green-50 border-l-4 border-green-500 p-4 mb-8">
			<p class="text-gray-700">
				SciLedger uses the CRediT (Contributor Roles Taxonomy) system to provide transparent 
				and standardized descriptions of author contributions to research articles.
			</p>
		</div>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">What is CRediT?</h2>
			<p class="text-gray-700 mb-4">
				CRediT is a high-level taxonomy that includes 14 roles that can be used to represent the 
				contributions typically made to scientific scholarly output. This system helps:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Clearly identify each author's contribution to the work</li>
				<li>Ensure appropriate credit and accountability</li>
				<li>Facilitate transparency in research collaboration</li>
				<li>Support career development through accurate attribution</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">The 14 CRediT Contributor Roles</h2>
			
			<div class="space-y-4">
				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">📋 Conceptualization</h3>
					<p class="text-gray-700 text-sm">
						Ideas; formulation or evolution of overarching research goals and aims.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">📊 Data Curation</h3>
					<p class="text-gray-700 text-sm">
						Management activities to annotate (produce metadata), scrub data and maintain research data 
						(including software code) for initial use and later re-use.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">📈 Formal Analysis</h3>
					<p class="text-gray-700 text-sm">
						Application of statistical, mathematical, computational, or other formal techniques to analyze or synthesize study data.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">💰 Funding Acquisition</h3>
					<p class="text-gray-700 text-sm">
						Acquisition of the financial support for the project leading to this publication.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">🔬 Investigation</h3>
					<p class="text-gray-700 text-sm">
						Conducting a research and investigation process, specifically performing the experiments, or data/evidence collection.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">🛠️ Methodology</h3>
					<p class="text-gray-700 text-sm">
						Development or design of methodology; creation of models.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">🎯 Project Administration</h3>
					<p class="text-gray-700 text-sm">
						Management and coordination responsibility for the research activity planning and execution.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">🏗️ Resources</h3>
					<p class="text-gray-700 text-sm">
						Provision of study materials, reagents, materials, patients, laboratory samples, animals, 
						instrumentation, computing resources, or other analysis tools.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">💻 Software</h3>
					<p class="text-gray-700 text-sm">
						Programming, software development; designing computer programs; implementation of the computer 
						code and supporting algorithms; testing of existing code components.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">👥 Supervision</h3>
					<p class="text-gray-700 text-sm">
						Oversight and leadership responsibility for the research activity planning and execution, 
						including mentorship external to the core team.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">✅ Validation</h3>
					<p class="text-gray-700 text-sm">
						Verification, whether as a part of the activity or separate, of the overall replication/reproducibility 
						of results/experiments and other research outputs.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">🎨 Visualization</h3>
					<p class="text-gray-700 text-sm">
						Preparation, creation and/or presentation of the published work, specifically visualization/data presentation.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">✍️ Writing - Original Draft</h3>
					<p class="text-gray-700 text-sm">
						Preparation, creation and/or presentation of the published work, specifically writing the initial draft 
						(including substantive translation).
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">📝 Writing - Review & Editing</h3>
					<p class="text-gray-700 text-sm">
						Preparation, creation and/or presentation of the published work by those from the original research group, 
						specifically critical review, commentary or revision – including pre- or post-publication stages.
					</p>
				</div>
			</div>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">How to Submit a CRediT Statement</h2>
			<p class="text-gray-700 mb-4">
				When submitting your manuscript to SciLedger, you will be asked to provide author contribution information:
			</p>
			<ol class="list-decimal list-inside space-y-2 text-gray-700 ml-4">
				<li>For each author, select all applicable CRediT roles from the list</li>
				<li>Multiple authors can have the same role</li>
				<li>Each author should have at least one role assigned</li>
				<li>The system will generate a formatted contribution statement</li>
			</ol>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Example CRediT Statement</h2>
			<div class="bg-blue-50 border border-blue-200 rounded-lg p-4">
				<p class="text-gray-800 italic mb-2">
					<strong>Author Contributions:</strong>
				</p>
				<p class="text-gray-700 text-sm">
					<strong>John Smith:</strong> Conceptualization, Methodology, Writing - Original Draft, Funding Acquisition. 
					<strong>Jane Doe:</strong> Data Curation, Formal Analysis, Visualization, Writing - Review & Editing. 
					<strong>Robert Johnson:</strong> Investigation, Resources, Writing - Review & Editing. 
					<strong>Maria Garcia:</strong> Software, Validation, Supervision.
				</p>
			</div>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Best Practices</h2>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Discuss and agree on contributions with all co-authors before submission</li>
				<li>Be honest and accurate about each person's contribution</li>
				<li>Consider all types of contributions, not just writing or data collection</li>
				<li>Update the statement if contributions change during revision</li>
				<li>Ensure all listed authors meet the criteria for authorship</li>
			</ul>
		</section>

