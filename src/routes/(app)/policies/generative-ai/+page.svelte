	<article class="prose prose-lg max-w-none">
		<h1 class="text-4xl font-bold text-gray-900 mb-4">Generative AI Policies</h1>
		
		<div class="bg-purple-50 border-l-4 border-purple-500 p-4 mb-8">
			<p class="text-gray-700">
				SciLedger recognizes the evolving role of artificial intelligence in research and scholarly publishing. 
				These policies provide guidance on the appropriate use and disclosure of AI tools.
			</p>
		</div>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">General Principles</h2>
			<p class="text-gray-700 mb-4">
				The use of generative AI tools (e.g., ChatGPT, Claude, GPT-4, etc.) in research and manuscript preparation 
				must be transparent, appropriate, and in compliance with these guidelines:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Authors are ultimately responsible for all content in their manuscripts</li>
				<li>AI tools cannot be listed as authors</li>
				<li>The use of AI must be properly disclosed</li>
				<li>AI-generated content must be verified for accuracy</li>
				<li>Original research and data must not be fabricated by AI</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Permitted Uses</h2>
			<p class="text-gray-700 mb-4">
				Generative AI tools may be used for the following purposes:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li><strong>Language improvement:</strong> Grammar checking, translation, and style enhancement</li>
				<li><strong>Literature review:</strong> Summarizing existing research and identifying relevant papers</li>
				<li><strong>Code generation:</strong> Assistance with data analysis scripts and programming</li>
				<li><strong>Figure preparation:</strong> Creating diagrams and visualizations</li>
				<li><strong>Brainstorming:</strong> Generating ideas for research directions or hypotheses</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Prohibited Uses</h2>
			<p class="text-gray-700 mb-4">
				The following uses of generative AI are not permitted:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li><strong>Data fabrication:</strong> Generating fake experimental results or data</li>
				<li><strong>Image manipulation:</strong> Creating or altering research images inappropriately</li>
				<li><strong>Plagiarism:</strong> Using AI to paraphrase or disguise copied content</li>
				<li><strong>Authorship attribution:</strong> Listing AI tools as authors</li>
				<li><strong>Peer review:</strong> Using AI to generate peer review comments without disclosure</li>
				<li><strong>Unverified claims:</strong> Including AI-generated statements without fact-checking</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Disclosure Requirements</h2>
			<p class="text-gray-700 mb-4">
				Authors must disclose the use of generative AI tools in their manuscripts:
			</p>
			
			<div class="bg-gray-50 border border-gray-200 rounded-lg p-4 mb-4">
				<h3 class="font-semibold text-gray-900 mb-2">Mandatory Disclosure</h3>
				<p class="text-gray-700 mb-2">
					Include a statement in the "Methods" or "Acknowledgments" section specifying:
				</p>
				<ul class="list-disc list-inside space-y-1 text-gray-700 ml-4">
					<li>Which AI tool(s) were used</li>
					<li>The specific purpose(s) for which they were used</li>
					<li>How the output was verified</li>
				</ul>
			</div>

			<div class="bg-blue-50 border border-blue-200 rounded-lg p-4">
				<h3 class="font-semibold text-gray-900 mb-2">Example Statement</h3>
				<p class="text-gray-700 italic">
					"The authors used ChatGPT (OpenAI) to improve the language and readability of this manuscript. 
					All AI-generated suggestions were reviewed and verified by the authors for accuracy and appropriateness."
				</p>
			</div>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Verification and Accuracy</h2>
			<p class="text-gray-700 mb-4">
				Authors must ensure that:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>All AI-generated content has been reviewed for accuracy</li>
				<li>Citations and references have been verified (AI tools can generate fake references)</li>
				<li>Scientific claims are supported by appropriate evidence</li>
				<li>The final manuscript reflects the authors' own understanding and interpretation</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">AI in Peer Review</h2>
			<p class="text-gray-700 mb-4">
				For reviewers using AI assistance:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Do not upload confidential manuscripts to public AI systems</li>
				<li>Disclose AI use in your review comments</li>
				<li>Ensure your review reflects your own expert judgment</li>
				<li>AI-generated reviews alone are not acceptable</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Future Developments</h2>
			<p class="text-gray-700">
				These policies will be updated regularly as AI technology and best practices evolve. 
				SciLedger is committed to fostering responsible innovation while maintaining research integrity.
			</p>
		</section>

		<div class="bg-yellow-50 border-l-4 border-yellow-500 p-4 mt-8">
