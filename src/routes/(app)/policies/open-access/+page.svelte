	<article class="prose prose-lg max-w-none">
		<h1 class="text-4xl font-bold text-gray-900 mb-4">Open Access Policy</h1>
		
		<div class="bg-indigo-50 border-l-4 border-indigo-500 p-4 mb-8">
			<p class="text-gray-700">
				SciLedger is committed to open access publishing, making research freely available 
				to readers worldwide without subscription barriers.
			</p>
		</div>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">What is Open Access?</h2>
			<p class="text-gray-700 mb-4">
				Open access means that research articles are:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Freely accessible online to anyone, anywhere</li>
				<li>Available without subscription or paywall barriers</li>
				<li>Licensed for broad reuse with proper attribution</li>
				<li>Permanently archived and preserved</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Benefits of Open Access</h2>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li><strong>Wider Reach:</strong> Research is accessible to global audiences</li>
				<li><strong>Greater Impact:</strong> Open access articles receive more citations</li>
				<li><strong>Public Access:</strong> Taxpayer-funded research is publicly available</li>
				<li><strong>Faster Discovery:</strong> No barriers to knowledge dissemination</li>
				<li><strong>Educational Value:</strong> Students and educators have free access</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Licensing Options</h2>
			<p class="text-gray-700 mb-4">
				SciLedger offers several Creative Commons licenses:
			</p>
			
			<div class="space-y-4">
				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">CC BY (Recommended)</h3>
					<p class="text-gray-700 text-sm">
						Allows others to distribute, remix, adapt, and build upon the work, even commercially, 
						as long as they credit the original creation.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">CC BY-NC</h3>
					<p class="text-gray-700 text-sm">
						Allows others to distribute, remix, adapt, and build upon the work non-commercially, 
						with credit to the original creation.
					</p>
				</div>

				<div class="bg-white border border-gray-200 rounded-lg p-4">
					<h3 class="font-semibold text-gray-900 mb-2">CC BY-ND</h3>
					<p class="text-gray-700 text-sm">
						Allows redistribution, commercial and non-commercial, as long as it is passed along 
						unchanged and in whole, with credit to the original creation.
					</p>
				</div>
			</div>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Copyright and Author Rights</h2>
			<p class="text-gray-700 mb-4">
				Under open access publishing on SciLedger:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Authors retain copyright of their work</li>
				<li>Authors grant SciLedger a license to publish and distribute</li>
				<li>Authors can reuse their work without requesting permission</li>
				<li>Authors can deposit preprints and postprints in repositories</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Publishing Fees</h2>
			<p class="text-gray-700 mb-4">
				SciLedger operates on a community-supported model:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>No article processing charges (APCs) for authors</li>
				<li>No subscription fees for readers</li>
				<li>Supported by institutional partnerships and grants</li>
				<li>Committed to sustainable open access</li>
			</ul>
		</section>

		<div class="bg-green-50 border-l-4 border-green-500 p-4 mt-8">
			<h3 class="font-semibold text-gray-900 mb-2">Our Commitment</h3>
			<p class="text-gray-700">
				SciLedger believes that research should be freely available to all. We are committed to 
				maintaining a sustainable open access platform that serves the global research community.
			</p>
		</div>
