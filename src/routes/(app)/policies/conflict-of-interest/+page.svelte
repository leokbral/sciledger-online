	<article class="prose prose-lg max-w-none">
		<h1 class="text-4xl font-bold text-gray-900 mb-4">Conflict of Interest Policy</h1>
		
		<div class="bg-red-50 border-l-4 border-red-500 p-4 mb-8">
			<p class="text-gray-700">
				Full disclosure of all potential conflicts of interest is essential for maintaining trust 
				and credibility in scholarly publishing.
			</p>
		</div>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">What is a Conflict of Interest?</h2>
			<p class="text-gray-700 mb-4">
				A conflict of interest exists when professional judgment concerning a primary interest 
				(such as research validity) may be influenced by a secondary interest (such as financial gain).
			</p>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Types of Conflicts</h2>
			
			<h3 class="text-xl font-semibold text-gray-800 mb-3 mt-6">Financial Conflicts</h3>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Employment or consultancy with interested parties</li>
				<li>Stock ownership or equity interests</li>
				<li>Patents or royalties related to the research</li>
				<li>Honoraria or speaker fees</li>
				<li>Research funding from commercial entities</li>
			</ul>

			<h3 class="text-xl font-semibold text-gray-800 mb-3 mt-6">Non-Financial Conflicts</h3>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Personal or professional relationships</li>
				<li>Academic competition</li>
				<li>Intellectual beliefs or passions</li>
				<li>Political or religious affiliations</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Disclosure Requirements</h2>
			<p class="text-gray-700 mb-4">
				All authors must disclose any conflicts of interest at the time of submission:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Complete a conflict of interest form for each author</li>
				<li>Include a conflict of interest statement in the manuscript</li>
				<li>Update disclosures if circumstances change during review</li>
				<li>Declare funding sources and their role in the research</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">For Reviewers</h2>
			<p class="text-gray-700 mb-4">
				Reviewers must decline to review manuscripts when they have:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Collaborated with authors in the past 3 years</li>
				<li>Financial interests in the outcome</li>
				<li>Strong personal or professional relationships with authors</li>
				<li>Competing research interests</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Example Statements</h2>
			<div class="space-y-4">
				<div class="bg-gray-50 border border-gray-200 rounded-lg p-4">
					<p class="text-sm font-semibold text-gray-800 mb-2">No Conflicts:</p>
					<p class="text-sm text-gray-700 italic">
						"The authors declare that they have no conflicts of interest."
					</p>
				</div>
				<div class="bg-gray-50 border border-gray-200 rounded-lg p-4">
					<p class="text-sm font-semibold text-gray-800 mb-2">Financial Conflict:</p>
					<p class="text-sm text-gray-700 italic">
						"Author A has received research funding from Company X. Authors B and C declare no conflicts of interest."
					</p>
				</div>
			</div>
		</section>
