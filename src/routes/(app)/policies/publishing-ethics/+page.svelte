	<article class="prose prose-lg max-w-none">
		<h1 class="text-4xl font-bold text-gray-900 mb-4">Publishing Ethics</h1>
		
		<div class="bg-blue-50 border-l-4 border-blue-500 p-4 mb-8">
			<p class="text-gray-700">
				SciLedger is committed to maintaining the highest standards of publication ethics and follows 
				the guidelines set by the Committee on Publication Ethics (COPE).
			</p>
		</div>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Research Integrity</h2>
			<p class="text-gray-700 mb-4">
				All submitted research must be original and not under consideration elsewhere. Authors must ensure that:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>The research has been conducted ethically and with integrity</li>
				<li>All data presented is authentic and has not been fabricated or manipulated</li>
				<li>Proper approval has been obtained from relevant ethics committees</li>
				<li>Informed consent has been obtained when required</li>
				<li>All sources and prior work are properly cited</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Authorship Guidelines</h2>
			<p class="text-gray-700 mb-4">
				Authorship should be limited to those who have made significant contributions to the work. All authors must:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Have made substantial contributions to conception, design, execution, or interpretation</li>
				<li>Have participated in drafting or revising the manuscript</li>
				<li>Have approved the final version to be published</li>
				<li>Agree to be accountable for all aspects of the work</li>
			</ul>
			<p class="text-gray-700 mt-4">
				Contributors who do not meet all criteria should be acknowledged but not listed as authors.
			</p>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Peer Review Ethics</h2>
			
			<h3 class="text-xl font-semibold text-gray-800 mb-3 mt-6">For Reviewers</h3>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Maintain confidentiality of the review process</li>
				<li>Declare any conflicts of interest</li>
				<li>Provide constructive, unbiased feedback</li>
				<li>Complete reviews in a timely manner</li>
				<li>Do not use information from manuscripts for personal advantage</li>
			</ul>

			<h3 class="text-xl font-semibold text-gray-800 mb-3 mt-6">For Editors</h3>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Ensure fair and unbiased peer review process</li>
				<li>Maintain confidentiality of manuscripts and reviews</li>
				<li>Declare and manage conflicts of interest</li>
				<li>Make decisions based on scientific merit</li>
				<li>Provide clear guidance to authors and reviewers</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Plagiarism and Duplication</h2>
			<p class="text-gray-700 mb-4">
				SciLedger has a zero-tolerance policy for plagiarism and duplicate publication:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>All manuscripts are checked for plagiarism using automated tools</li>
				<li>Text, figures, or data from other sources must be properly attributed</li>
				<li>Self-plagiarism and duplicate submission are not permitted</li>
				<li>Violations may result in manuscript rejection and author sanctions</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Handling Misconduct</h2>
			<p class="text-gray-700 mb-4">
				If research misconduct is suspected or alleged, SciLedger will:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li>Investigate all allegations thoroughly and confidentially</li>
				<li>Follow COPE guidelines for handling misconduct</li>
				<li>Take appropriate action, including article retraction if necessary</li>
				<li>Notify relevant institutions and authorities when required</li>
				<li>Publish corrections or retractions with clear explanations</li>
			</ul>
		</section>

		<section class="mb-8">
			<h2 class="text-2xl font-semibold text-gray-900 mb-4">Post-Publication Corrections</h2>
			<p class="text-gray-700 mb-4">
				SciLedger is committed to maintaining the integrity of the published record:
			</p>
			<ul class="list-disc list-inside space-y-2 text-gray-700 ml-4">
				<li><strong>Corrections:</strong> For minor errors that do not affect conclusions</li>
				<li><strong>Expressions of Concern:</strong> When serious concerns are raised about validity</li>
				<li><strong>Retractions:</strong> For serious misconduct or fundamental errors</li>
			</ul>
		</section>

		<div class="bg-yellow-50 border-l-4 border-yellow-500 p-4 mt-8">
			<h3 class="font-semibold text-gray-900 mb-2">Reporting Concerns</h3>
			<p class="text-gray-700">
				If you have concerns about the ethical conduct of research published on SciLedger, 
				please contact us at <a href="mailto:ethics@sciledger.org" class="text-primary-500 hover:text-primary-600">ethics@sciledger.org</a>
			</p>
		</div>
